    ObjectBuilder::ObjectBuilder()
        : result_{}
        , fresh_(true)
    {
        result_ << "{";
    }
//---------------------------------------------------------------------------------------------------------------------
    void ObjectBuilder::comma()
    {
        if (!fresh_)
        {
            result_ << ",";
        }
        else
            fresh_ = false;
    }
